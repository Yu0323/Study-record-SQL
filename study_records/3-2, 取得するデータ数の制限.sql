-- 末尾にLIMIT句を付けることで取得するデータの数を制限
SELECT * FROM test_scores LIMIT 2;
-- 結果
/*
+------+------+---------+------+---------+
| id   | name | english | math | science |
+------+------+---------+------+---------+
|    1 | Yu   | 98      | 97   | 65      |
|    2 | Ai   | 96      | 80   | 76      |
+------+------+---------+------+---------+
